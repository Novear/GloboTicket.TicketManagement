namespace GloboTicket.TicketManagement.Application.Profiles
{
    // it os just a simple class that contains what automapper should be 
    // knowing in terms of mappings between types it does need to inherit from 
    // tha base Profile class that is a class that comes with AutoMapper.
    public class MappingProfile:Profile
    {
        // in the constructor I am going to write my mappings so I am going to create 
        // a mapping between Event and EventListVm and I am going to specify that 
        // it has to be capable of knowing about this mapping in the two ways
        // so from Event to EventListVm and vice versa.
        // that I am doing here using the ReverseMap
        // we will add more mapping profiles as we go along.
        // 
        public MappingProfile()
        {
            CreateMap<Event, EventListVM>().ReverseMap();
            CreateMap<Event, CreateEventCommand>().ReverseMap();
            CreateMap<Event, UpdateEventCommand>().ReverseMap();
            CreateMap<Event, EventDetailVM>().ReverseMap();
            CreateMap<Event, CategoryEventDto>().ReverseMap();
            CreateMap<Event, EventExportDto>().ReverseMap();

            CreateMap<Category, CategoryDto>();
            CreateMap<Category, CategoryListVm>();
            CreateMap<Category, CategoryEventListVm>();
            CreateMap<Category, CreateCategoryCommand>();
            CreateMap<Category, CreateCategoryDto>();
        }
    }
}
